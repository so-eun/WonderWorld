package wonderWorld.event;

public class EventBook {
	private int ap_id;
	private String ap_name;
	private int ap_numOfMem;
	private String ap_memo;
	
	public int getAp_id() {
		return ap_id;
	}
	public void setAp_id(int ap_id) {
		this.ap_id = ap_id;
	}
	public String getAp_name() {
		return ap_name;
	}
	public void setAp_name(String ap_name) {
		this.ap_name = ap_name;
	}
	public int getAp_numOfMem() {
		return ap_numOfMem;
	}
	public void setAp_numOfMem(int ap_numOfMem) {
		this.ap_numOfMem = ap_numOfMem;
	}
	public String getAp_memo() {
		return ap_memo;
	}
	public void setAp_memo(String ap_memo) {
		this.ap_memo = ap_memo;
	}
}
